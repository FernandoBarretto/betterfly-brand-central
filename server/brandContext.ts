export function buildPlaybookContext(audience: string): string {
  const playbook = playbooks[audience as keyof typeof playbooks];
  if (!playbook) return "";

  return `
=== ${playbook.title.toUpperCase()} PLAYBOOK ===
Value Proposition: ${playbook.valueProp}

Messaging Pillars:
${playbook.pillars.map((p, i) => `${i + 1}. ${p.title}: ${p.desc}`).join("\n")}

Approved Messages:
${playbook.messages.map((m) => `- [${m.label}]: "${m.content}"`).join("\n")}

Proof Points (use ONLY these — do not invent statistics):
${playbook.proofPoints.map((p) => `• ${p}`).join("\n")}

What to Avoid:
${playbook.avoid.map((a) => `✗ ${a}`).join("\n")}
`.trim();
}

